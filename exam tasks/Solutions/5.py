def diff(a, b):
    return list(set(a).difference(set(b)))

print(diff([1,2], [1])) # [2]
print(diff([1,2,2,2,3], [2,3])) # [1]
print(diff([18, 8, -14], [8, -20, -16, -18])) # [18, -14]
print(diff([6, -9, -1], [-3, -4, 17, -4, 10, -16, -12, 11, -2, 17, 2, 1, 6])) # [-9, -1]
print(diff([-8, -2, -4, 13, 10, -12, -3, 6], [-3, 17])) # [-8, -2, -4, 13, 10, -12, 6]